// --- Toast System ---
const ToastContext = createContext();

export function ToastProvider({ children }) {
  const [toasts, setToasts] = useState([]);
  
  const removeToast = useCallback((id) => {
    setToasts(prev => prev.filter(t => t.id !== id));
  }, []);

  const addToast = useCallback((message, type = "info") => {
    const id = crypto.randomUUID();
    setToasts(prev => [...prev, { id, message, type }]);
    setTimeout(() => removeToast(id), 4000);
  }, [removeToast]);

  return (
    <ToastContext.Provider value={{ addToast }}>
      {children}
      <div className="fixed bottom-4 right-4 flex flex-col gap-2 z-50">
        {toasts.map(t => (
          <div 
            key={t.id} 
            className={`px-4 py-3 rounded shadow-lg border text-sm font-medium animate-in slide-in-from-bottom-2 fade-in duration-300
              ${t.type === 'error' ? 'bg-red-900/90 border-red-700 text-white' : 
                t.type === 'success' ? 'bg-green-900/90 border-green-700 text-white' : 
                'bg-gray-800 border-gray-700 text-white'}`}
          >
            {t.message}
          </div>
        ))}
      </div>
    </ToastContext.Provider>
  );
}

export const useToast = () => useContext(ToastContext);

